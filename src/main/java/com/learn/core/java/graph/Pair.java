package com.learn.core.java.graph;

public class Pair {
	
	private int key;
	private int value;
	public Pair(int a, int b) {
		key = a;
		value = b;
	}
	public int getKey() {
		return key;
	}
	public void setKey(int key) {
		this.key = key;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}

	
}
